module Abstractions where


{-

Haskell is exhibits powerful Abstractions that which:

* Correspond to specific and common programming patterns.
* Allow us to program a high level generality.
* Minimize boilerplate (noise, repetitive code)
* Facilitate composition.

Abstractions are implemented via Type-Classes which specific types and Contexts instantiate. An `instance` expresses how the type implements whichever functionality characterizes the given abstraction.

Some abstractions include:

Functor
Semigroup
Monoids
Monads


-}